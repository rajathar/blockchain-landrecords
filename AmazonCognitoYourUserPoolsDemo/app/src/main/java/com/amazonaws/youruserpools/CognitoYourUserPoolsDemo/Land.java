package com.amazonaws.youruserpools.CognitoYourUserPoolsDemo;

public class Land {

    private String assetId;
    private String address;
    private String assetNorthCoordinatesId;
    private String assetEastCoordinatesId;
    private String assetWestCoordinatesId;
    private String assetSouthCoordinatesId;
    private String ownerId;
    private String ownerName;

    public String getAssetId() {
        return assetId;
    }

    public void setAssetId(String assetId) {
        this.assetId = assetId;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAssetNorthCoordinatesId() {
        return assetNorthCoordinatesId;
    }

    public void setAssetNorthCoordinatesId(String assetNorthCoordinatesId) {
        this.assetNorthCoordinatesId = assetNorthCoordinatesId;
    }

    public String getAssetEastCoordinatesId() {
        return assetEastCoordinatesId;
    }

    public void setAssetEastCoordinatesId(String assetEastCoordinatesId) {
        this.assetEastCoordinatesId = assetEastCoordinatesId;
    }

    public String getAssetWestCoordinatesId() {
        return assetWestCoordinatesId;
    }

    public void setAssetWestCoordinatesId(String assetWestCoordinatesId) {
        this.assetWestCoordinatesId = assetWestCoordinatesId;
    }

    public String getAssetSouthCoordinatesId() {
        return assetSouthCoordinatesId;
    }

    public void setAssetSouthCoordinatesId(String assetSouthCoordinatesId) {
        this.assetSouthCoordinatesId = assetSouthCoordinatesId;
    }

    public String getOwnerId() {
        return ownerId;
    }

    public void setOwnerId(String ownerId) {
        this.ownerId = ownerId;
    }

    public String getOwnerName() {
        return ownerName;
    }

    public void setOwnerName(String ownerName) {
        this.ownerName = ownerName;
    }
}
